#include<stdio.h>
int main(){
    int a,b;
    printf("Enter first integer:");
    scanf("%d",&a);
    printf("\nEnter second integer:");
    scanf("%d",&b);
    printf("\nSum is %d",a+b); // for summation
    printf("\nDifference is %d",a-b); // for subtraction
    printf("\nProduct is %d",a*b); // for multiplication
    return 0; 

}